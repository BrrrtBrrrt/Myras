namespace Myras.Enums
{
    /// <summary>
    /// Enum representing different types of layers used in neural networks.
    /// </summary>
    public enum LayerType
    {
        /// <summary>
        /// Represents the input layer, which is the first layer of a neural network 
        /// that receives input data.
        /// </summary>
        INPUT,

        /// <summary>
        /// Represents a dense (fully connected) layer, where each neuron is connected 
        /// to every neuron in the previous layer.
        /// </summary>
        DENSE,
    }
}
